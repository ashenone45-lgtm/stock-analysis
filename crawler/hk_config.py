def get_all_codes() -> list[str]:
    """返回所有港股代码列表（去重）"""
    seen: set[str] = set()
    result: list[str] = []
    for stocks in HK_STOCK_POOL.values():
        for code, _ in stocks:
            if code not in seen:
                seen.add(code)
                result.append(code)
    return result


def get_industry_map() -> dict[str, str]:
    """返回 {code: 板块标签}（跨列股票取第一个板块）"""
    result: dict[str, str] = {}
    for industry, stocks in HK_STOCK_POOL.items():
        for code, _ in stocks:
            if code not in result:
                result[code] = industry
    return result
